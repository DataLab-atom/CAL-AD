from numpy.linalg import inv
from quadratic_function import QuadraticFunction
import numpy as np
def search_root(qf: QuadraticFunction, x0: np.ndarray, tol: float = 1e-6, max_iter: int = 1000, k: int = 5) -> np.ndarray:
    """
    Optimizes a quadratic objective function using an enhanced iterative algorithm. 

    Parameters:
    - qf: An instance of QuadraticFunction.
    - x0: The initial guess for the variable to minimize over.
    - tol: Tolerance for detecting convergence, default is 1e-6.
    - max_iter: The maximum number of iterations allowed, default is 1000.
    - k: Parameter for how to handle subsets in updates, default is 5 (for flexibility in updates).
    
    Returns:
    - Optimized variable vector x that minimizes the quadratic function.
    """
    d = qf.d  # Number of dimensions
    n = qf.n  # Number of defining quadratic functions
    
    x = x0.copy()  # Initialize current variable vector
    B = [np.eye(d) for _ in range(n)]  # Initialize Hessians (inverse) as identity matrices

    for t in range(max_iter):
        it = t % n  # Cyclic selection of quadratic functions
        
        # Compute the approximate total gradient and Hessian
        grad = np.sum([qf.gradient(x) for i in range(n)], axis=0)
        B_agg = np.sum(B, axis=0)

        # Update direction with aggregated Hessian
        B_inv = np.linalg.inv(B_agg)
        direction = -B_inv @ grad

        # Enhance with a basic armijo line search for step control
        alpha = 1.0
        while alpha > 1e-8:
            x_new = x + alpha * direction
            if qf.objective_function(x_new) < qf.objective_function(x):
                break  # Sufficient descent, break to accept the step
            alpha *= 0.5  # Reduce step size

        # Check for convergence
        if np.linalg.norm(x_new - x) < tol:
            return x_new
