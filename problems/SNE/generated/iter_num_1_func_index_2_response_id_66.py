def search_root(qf: QuadraticFunction, x0: np.ndarray, tol: float = 1e-6, max_iter: int = 1000, k: int = 5) -> np.ndarray:
    d = qf.d  # Dimension of x
    n = qf.n  # Number of quadratic functions
    
    x = x0.copy()  # Initial guess
    B = [np.eye(d) for _ in range(n)]  # Initialize approximations for Hessians
    
    for t in range(max_iter):
        it = t % n  # Cycle through the function index for updates
        
        # Gradients and approximated Hessian accumulation
        gradient_sum = np.sum([qf.gradient(x) for _ in range(n)], axis=0)
        B_agg = sum(B)  # Aggregate Hessian approximation
        
        try:
            B_inv = np.linalg.inv(B_agg)  # Compute the inverse of the aggregated Hessian
            delta_x = -np.dot(B_inv, gradient_sum)  # Descent direction
        except np.linalg.LinAlgError:
            print("Singular matrix encountered; using identity matrix instead.")
            B_inv = np.eye(d)
            delta_x = -np.dot(B_inv, gradient_sum)

        # Adaptive step size with backtracking line search
        step_size = 1.0
        while step_size > 1e-8:
            x_new = x + step_size * delta_x
            if qf.objective_function(x_new) < qf.objective_function(x):
                break
            step_size *= 0.5  # Halve step size
        
        if np.linalg.norm(x_new - x) < tol:
            print(f"Converged at iteration {t}")
            return x_new